import java.io.*;
import java.util.*;
class Person {
    String firstName, lastName, ssn, id, password, phoneNumber, email;
// constructor
    public Person(String firstName, String lastName, String ssn, String id, String password, String phoneNumber, String email) {
        this.firstName = firstName;
        this.lastName = lastName;
        this.ssn = ssn;
        this.id = id;
        this.password = password;
        this.phoneNumber = phoneNumber;
        this.email = email;
    }
}

